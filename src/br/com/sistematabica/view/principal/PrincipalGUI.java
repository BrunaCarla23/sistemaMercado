    public int activeClickedSubMenu(int controlButtonCliicked, JPanel panelSubMenu, JPanel JPanelIconMenu, JPanel JPanelTextMenu) {
        if(controlButtonCliicked == 1) {
            controlButtonCliicked = 0;
            panelSubMenu.setVisible(false);
            
            return controlButtonCliicked;
            
        }else if(controlButtonCliicked == 0) {
            
            style.jPanelEditColor(JPanelIconMenu, 8, 18, 38);
            style.jPanelEditColor(JPanelTextMenu, 8, 18, 38);
            
            controlButtonCliicked = 1;
            panelSubMenu.setVisible(true);
            return controlButtonCliicked;
        }  
        return controlButtonCliicked;
    }
    
    
